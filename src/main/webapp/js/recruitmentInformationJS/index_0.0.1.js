    function doSearch() {
        const params = buildParams(currentPage);
        showLoading(true);
        clearCards();

        $.ajax({
            url: '/recruitmentInformation/search',
            type: 'GET',
            data: params,
            dataType: 'json',
            traditional: true,   // 배열 파라미터 region[]=...&region[]=... 방지 → region=...&region=...
            success: function (resp) {
                showLoading(false);
                renderResults(resp);
            },
            error: function () {
                showLoading(false);
                showEmpty(true);
                showToast('채용공고 조회 중 오류가 발생했습니다.', 'error');
            }
        });
    }

    /* ============================================================
     *  파라미터 빌드
     * ============================================================ */
    function buildParams(page) {
        const p = {
            startPage   : page,
            display     : $('#display').val()    || 10,
            sortOrderBy : $('#sortOrderBy').val() || 'DESC'
        };

        // 다중: 지역 (region_nm LIKE 텍스트 검색)
        const regionNames = selectedRegions
            .filter(function (r) { return r.sidoName !== '지역무관'; })
            .map(function (r) {
                if (r.name.endsWith('전체') || r.name === r.sidoName) return r.sidoName;
                return r.name;
            });
        if (regionNames.length) p.region = regionNames;

        // 다중: 직종 (대분류/중분류 선택 시 소분류 코드로 확장)
        var expandedOccCodes = [];
        selectedOccupations.forEach(function (o) {
            var code = String(o.code);
            // 대분류 체크
            var cat = OCC_DATA.find(function (c) { return c.code === code; });
            if (cat) {
                cat.subs.forEach(function (sub) {
                    if (sub.subs) sub.subs.forEach(function (d) { expandedOccCodes.push(d.code); });
                });
                return;
            }
            // 중분류 체크
            var foundSub = null;
            OCC_DATA.forEach(function (c) {
                c.subs.forEach(function (s) { if (s.code === code) foundSub = s; });
            });
            if (foundSub && foundSub.subs) {
                foundSub.subs.forEach(function (d) { expandedOccCodes.push(d.code); });
                return;
            }
            // 소분류 코드 그대로 사용
            expandedOccCodes.push(code);
        });
        var uniqueOccCodes = expandedOccCodes.filter(function (v, i, a) { return a.indexOf(v) === i; });
        if (uniqueOccCodes.length) p.occupation = uniqueOccCodes;

        // 다중: 학력
        const eduVals = getCheckedVals('#educationGroup');
        if (eduVals.length) p.education = eduVals;

        // 다중: 경력
        const careerVals = getCheckedVals('#careerGroup');
        if (careerVals.length) p.career = careerVals;

        // 단일 셀렉트
        const empTp  = $('#empTp').val();
        const salTp  = $('#salTp').val();
        const minPay = $('#minPay').val();
        const maxPay = $('#maxPay').val();
        const holiday= $('#holidayTp').val();
        const regDate= $('#regDate').val();
        const keyword= $.trim($('#keyword').val());

        if (empTp)   p.empTp   = empTp;
        if (salTp) {
            p.salTp = salTp;
            if (minPay) p.minPay = minPay;
            if (maxPay) p.maxPay = maxPay;
        }
        if (holiday) p.holidayTp      = holiday;
        if (regDate) p.regDate        = regDate;
        if (keyword) p.keyword        = keyword;

        return p;
    }

    function getCheckedVals(groupSelector) {
        const vals = [];
        $(groupSelector + ' .cb-item.checked').each(function () {
            vals.push($(this).data('val'));
        });
        return vals;
    }

