    function renderResults(resp) {
        const list  = resp.wantedInfo || [];
        const total = resp.total      || 0;

        if (list.length === 0) {
            showEmpty(true);
            $('#resultsHeader,#paginationWrap').hide();
            return;
        }

        $('#totalCount').text(total.toLocaleString());
        $('#resultsHeader').css('display', 'flex');

        const $grid = $('#jobCardsGrid');
        list.forEach(function (job, idx) {
            $grid.append(buildJobCard(job, idx));
        });

        renderPagination(total, currentPage, parseInt($('#display').val()));
    }

