    function renderPagination(total, page, perPage) {
        const totalPages = Math.ceil(total / perPage);
        if (totalPages <= 1) { $('#paginationWrap').hide(); return; }
        const $btns = $('#paginationBtns').empty();
        const block = 10;
        const start = Math.floor((page - 1) / block) * block + 1;
        const end   = Math.min(start + block - 1, totalPages);
        if (start > 1) $btns.append(pagBtn('<i class="bi bi-chevron-left"></i>', start - 1));
        for (let i = start; i <= end; i++) {
            const $b = pagBtn(i, i);
            if (i === page) $b.addClass('active');
            $btns.append($b);
        }
        if (end < totalPages) $btns.append(pagBtn('<i class="bi bi-chevron-right"></i>', end + 1));
        $('#paginationWrap').show();
    }
    function pagBtn(label, pageNum) {
        return $('<a class="page-btn"></a>').html(label).on('click', function () {
            currentPage = pageNum;
            clearCards();
            doSearch();
            $('html,body').animate({ scrollTop: $('.results-section').offset().top - 80 }, 300);
        });
    }

